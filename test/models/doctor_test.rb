require 'test_helper'

class DoctorTest < ActiveSupport::TestCase
  test "valid doctor" do
    doctor = Doctor.createDoctor(first_name: 'test', last_name: 'test', email: 'test@hotmail.com', password: 'testest')
    assert User.where(email: "test@hotmail.com"), "valid Doctor"
  end

  test "invalid Doctor" do
    doctor = Doctor.createDoctor(first_name: 'test', last_name: 'test', email: 'test@hotmail.com')
    assert_not_nil User.where(email: "test@hotmail.com"), "invalid Doctor"
  end

  test "valid doctor get user" do
    doctor = Doctor.createDoctor(first_name: 'test', last_name: 'test', email: 'test@hotmail.com', password: 'testest')
    assert doctor.user, "valid doctor get user"
  end

  test "invalid Doctor get user" do
    doctor = Doctor.createDoctor(first_name: 'test', last_name: 'test', email: 'test@hotmail.com')
    assert_not doctor.user(), "invalid doctor get user"
  end

end